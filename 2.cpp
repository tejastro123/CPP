// input-output,variables,datatypes,operators,unaryOperators.
#include <iostream>
using namespace std;
int main() {
    int a = 5, b = 4 ;
    
    cout << "Sum = " << (a+b) << endl;
    cout << "Difference = " << (a-b) << endl;
    cout << "Product = " << (a*b) << endl;
    cout << "Quotient = " << (a/b) << endl;
    cout << "Remainder = " << (a%b) << endl;

    cout << (a<b) << endl;
    cout << (a>b) << endl;
    cout << (a==b) << endl;
    cout << (a!=b) << endl;
    cout << (a<=b) << endl;
    cout << (a>=b) << endl;

    cout << "Before swapping: a = " << a << ", b = " << b << endl;
    a = a + b;
    b = a - b;
    a = a - b;
    cout << "After swapping: a = " << a << ", b = " << b << endl;
    
    
    return 0;
 
}